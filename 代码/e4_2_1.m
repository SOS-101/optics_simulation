clc;
clear;
N=1024;
I64=zeros(N,N);%预定义平面I64的灰度值为0
[m,n]=meshgrid(linspace(-N/2,N/2-1,N));%确定坐标系及坐标原点的位置
r=input('请输入圆的半径r=');%输入圆的半径大小控制变量r(单位Pixel)
a=input('请输入圆心位置的控制变量a=');%输入圆心位置控制变量a
b=input('请输入圆心位置的控制变量b=');%输入圆心位置控制变量b
D=((m+a).^2+(n+b).^2).^(1/2);%圆函数关系式
i=find(D<=r);%f返回满足条件D<=r的像素点的单下标值
I64(i)=1;%像素点赋值
imshow(I64);%显示图像
