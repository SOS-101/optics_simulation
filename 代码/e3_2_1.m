w1=50;       %波1频率
w2=60;       %波2频率
k1=5;        %波1波数
k2=4;        %波2波数
t=0.1:0.2:1.3;      %对时间进行等间隔取点
a=1;                %波动振幅
x=0:0.001:5;        %对传播方向x轴进行等间隔取点
A2=a*cos(k2*x-w2*t(end));      %A2波动函数
A1=a*cos(k1*x-w1*t(end));      %A1波动函数
plot(x,A1,'-',x,A2,':')
set(gcf,'color',[1 1 1]);
set(gca,'YTick',[-1:0.5:1]);
set(gca,'XTick',[0:1:5]);
xlabel('变量X')
ylabel('振幅')
title('两列单色平面波的模拟')
legend('光波1','光波2')
